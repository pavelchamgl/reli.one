import { toast, Bounce } from "react-toastify";
import "react-toastify/dist/ReactToastify.css"; // Импорт стилей для корректного отображения

export const AuthNeed = (text) => {
  toast.error(text, {
    position: "top-right",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
    transition: Bounce,
  });
};

